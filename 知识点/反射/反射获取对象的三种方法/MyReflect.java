package 反射.反射获取对象的三种方法;

public class MyReflect {
    //静态方法
    public static void staticMd(){
        System.out.println("静态方法");
    }

    //公共方法
    public static void publicMd(){
        System.out.println("公共方法");
    }

    //私有方法
    private static void privateMd(){
        System.out.println("私有方法");
    }
}
